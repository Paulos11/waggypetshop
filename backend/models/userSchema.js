const mongoose = require('mongoose');
const modelConfig = require('../config/modelConfig');

const userSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true,
    unique: true,
    trim: true
  },
  password: {
    type: String,
    required: true,
    select: false
  },
  role: {
    type: String,
    enum: ['user', 'admin'],
    default: 'user'
  }
}, modelConfig);

const User = mongoose.model('User', userSchema);

module.exports = User;
